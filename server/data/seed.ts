export function recomputeScores() {
  videos = videos.map((v) => ({
    ...v,
    impactScore: calculateImpactScore(
      INITIAL_DATA.find((i) => i.title === v.title)?.impactScore || 80,
      v.categorySlug,
      v.title,
      v.isTrending
    ),
  }));
}

